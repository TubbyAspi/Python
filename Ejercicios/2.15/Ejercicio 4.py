# Asume que ejecutamos las siguientes sentencias de asignación:
# ancho = 17
# alto = 12.0
# Para cada una de las expresiones siguientes, escribe el valor de la expresión y el
# tipo (del valor de la expresión).
# 1. ancho/2
# 2. ancho/2.0
# 3. alto/3
# 4. 1 + 2 * 5

ancho = 17
alto = 12.0

# 1
print(type(ancho/2), "ancho =", ancho, "/2 = ", ancho/2)

# 2
print(type(ancho/2.0), "ancho =", ancho, "/2.0 = ", ancho/2.0)

# 3
print(type(alto/3), "alto =", alto, "/3 = ", alto/3)

# 4
